    private void limpiar(){
        jtxtDUI.setText("");
        jtxtNombre.setText("");
        jtxtApellido.setText("");
        jspnEdad.setValue(0);
        jcbCargo.setSelectedIndex(0);
        jrbHombre.setSelected(true);
        jcxbEstadoCivil.setSelected(false);
    }
    
