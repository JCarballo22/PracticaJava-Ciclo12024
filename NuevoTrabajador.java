    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        // TODO add your handling code here:
       try{
           Trabajador t = new Trabajador();
           t.setDui(jtxtDUI.getText());
           t.setNombre(jtxtNombre.getText());
           t.setApellido(jtxtApellido.getText());
           t.setEdad(Integer.parseInt(jspnEdad.getValue().toString()));
           t.setCargo(jcbCargo.getSelectedItem().toString());
           t.setGenero(jrbHombre.isSelected());
           t.setEstadoCivil(jcxbEstadoCivil.isSelected());
           
           TrabajadorControl tControl = new TrabajadorControl();
           
           if(tControl.agregarTrabajador(t)){
               JOptionPane.showMessageDialog(this, "Guardado exitoso");
           }else{
               JOptionPane.showMessageDialog(this, "No se guardo");
           }
           
           this.limpiar();
           
       }catch(Exception ex){
           JOptionPane.showMessageDialog(this, ex.getMessage());
       } 
       
       
        
    }//GEN-LAST:event_jbtnGuardarActionPerformed

